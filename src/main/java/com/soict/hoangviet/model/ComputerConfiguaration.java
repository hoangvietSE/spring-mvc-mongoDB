	public String getSystemManufactor() {
		return systemManufactor;
	}
	public void setSystemManufactor(String systemManufactor) {
		this.systemManufactor = systemManufactor;
	}
	public String getSystemModel() {
		return systemModel;
	}
	public void setSystemModel(String systemModel) {
		this.systemModel = systemModel;
	}
