package com.soict.hoangviet.model;

public class RecentFile {
	private String fileName;
	private String extension;
	private String order;
	private String openTime;
	private String fileModifiedTime;
	private String fileCreatedTime;
	private String fileSize;
	private String fileAttributes;
	private String fileOwner;
	private String filenameOnly;
	private String date;
	private String computer;
	
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getComputer() {
		return computer;
	}
	public void setComputer(String computer) {
		this.computer = computer;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public String getExtension() {
		return extension;
	}
	public void setExtension(String extension) {
		this.extension = extension;
	}
	public String getOrder() {
		return order;
	}
	public void setOrder(String order) {
		this.order = order;
	}
	public String getOpenTime() {
		return openTime;
	}
	public void setOpenTime(String openTime) {
		this.openTime = openTime;
	}
	public String getFileModifiedTime() {
		return fileModifiedTime;
	}
	public void setFileModifiedTime(String fileModifiedTime) {
		this.fileModifiedTime = fileModifiedTime;
	}
	public String getFileCreatedTime() {
		return fileCreatedTime;
	}
	public void setFileCreatedTime(String fileCreatedTime) {
		this.fileCreatedTime = fileCreatedTime;
	}
	public String getFileSize() {
		return fileSize;
	}
	public void setFileSize(String fileSize) {
		this.fileSize = fileSize;
	}
	public String getFileAttributes() {
		return fileAttributes;
	}
	public void setFileAttributes(String fileAttributes) {
		this.fileAttributes = fileAttributes;
	}
	public String getFileOwner() {
		return fileOwner;
	}
	public void setFileOwner(String fileOwner) {
		this.fileOwner = fileOwner;
	}
	public String getFilenameOnly() {
		return filenameOnly;
	}
	public void setFilenameOnly(String filenameOnly) {
		this.filenameOnly = filenameOnly;
	}
	
	
}
